"""
Log Hunter - Semantic Quagmire v10
5M tokens of log data. Find 3 hacker IPs through semantic understanding.
- ALL reasons generated by LLM dynamically
- Usernames from SHARED POOL (both hacker and normal use same pool, randomly assigned)
- Cannot distinguish hacker/normal by username - must use semantic analysis of reasons
- Regex cannot match, must use LLM semantic understanding
- Requires MapReduce architecture to process 5M tokens
"""

import random
import os
import asyncio

# LLM client setup
try:
    from openai import AsyncOpenAI
    HAS_OPENAI = True
except ImportError:
    HAS_OPENAI = False


def get_llm_client():
    """Get LLM client from environment"""
    if not HAS_OPENAI:
        return None
    api_key = os.getenv("LLM_API_KEY") or os.getenv("OPENAI_API_KEY")
    base_url = os.getenv("LLM_BASE_URL", "https://api.deepseek.com")
    if not api_key:
        return None
    return AsyncOpenAI(api_key=api_key, base_url=base_url)


def generate_random_ip() -> str:
    """Generate random IP"""
    return f"{random.randint(1,255)}.{random.randint(0,255)}.{random.randint(0,255)}.{random.randint(1,254)}"


